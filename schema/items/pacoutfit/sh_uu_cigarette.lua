local SMOKE_TIME_BLUE = 60 * 3

function ITEM:PaintOver(item, w, h)
	local equipped = item:GetData("equip", false)
	local timeLeft = 1

	local startTime = item:GetData("startTime", CurTime())
	local elapsed = item:GetData("elapsed", 0)

	if equipped then
		timeLeft = (1 - ((CurTime() + elapsed) - startTime) / SMOKE_TIME_BLUE)
	else
		timeLeft = (SMOKE_TIME_BLUE - elapsed) / SMOKE_TIME_BLUE
	end

    if timeLeft != nil then
        surface.SetDrawColor(equipped and HSVToColor((.25 * 360) * timeLeft, 1, 1) or Color(118,118,118))
        surface.DrawRect(2, h - 4, (w - 4) * timeLeft, 2)
    end

	if (equipped) then
		surface.SetDrawColor(110, 255, 110, 100)
		surface.DrawRect(w - 14, h - 14, 8, 8)
	end
end